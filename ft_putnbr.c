void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		ft_putnbr(-nb / 10);
		ft_putchar(8 + '0');
	}
	else
	{
		if (nb < 0)
		{
			ft_putchar('-');
			nb = -nb;
		}
		if (nb > 9)
		{
			ft_putnbr(nb / 10);
			ft_putnbr(nb % 10);
		}
		else
			ft_putchar(nb + '0');
	}
}
