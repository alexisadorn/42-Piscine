#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		main(void)
{
	int		x;
	int		y;
	
	x = 25;
	y = 24;
	while (x >= 0)
	{
		ft_putchar(x + 'a');
		ft_putchar(y + 'A');
		x = x - 2;
		y = y - 2;
	}
	ft_putchar('\n');
}
