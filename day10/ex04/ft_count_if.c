int		ft_count_if(char **tab, int(*f)(char*))
{
	int b;
	int count;

	b = 0;
	count = 0;
	while (tab[b] != 0)
	{
		if (f(tab[b]) == 1)
			count++;
		b++;
	}
	return (count);
}
